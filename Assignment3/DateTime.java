package hw3;

public class DateTime {
    private int year;
    private int month;
    private int date;
    private int hour;
    private int minute;

    // Constructor to initialize a new DateTime object
    public DateTime(int year, int month, int date, int hour, int minute) {
        this.year = year;
        this.month = month;
        this.date = date;
        this.hour = hour;
        this.minute = minute;
    }
}//end of the DataTime.java