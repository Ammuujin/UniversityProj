package hw3;
//Resident.java
class Resident {
    private String name;
    private String phoneNumber;

    public Resident(String name, String phoneNumber) {
        this.name = name;
        this.phoneNumber = phoneNumber;
    }

    public String getName() {
        return name;
    }
    public String getPhoneNumber(){
        return phoneNumber;
    }
}//End of the Resident.java